def sigungu_centroid(
    region: Optional[str], sigungu: Optional[str]
) -> tuple[Optional[float], Optional[float]]:
    """(광역, 시군구) → 시군구 중심 좌표. 없으면 (None, None)."""
    if not sigungu:
        return None, None
    sg = sigungu.strip().split()[0]  # '진영읍' 같은 하위 표기는 앞 토큰만
    # 우선 광역+시군구 조합(중복명 방지)
    if region:
        key = f"{region}|{sg}"
        if key in AMBIGUOUS_SIGUNGU:
            return AMBIGUOUS_SIGUNGU[key]
    if sg in SIGUNGU_CENTROIDS:
        return SIGUNGU_CENTROIDS[sg]
    # '시/군/구' 접미사 보정
    for suffix in ("시", "군", "구"):
        if sg + suffix in SIGUNGU_CENTROIDS:
            return SIGUNGU_CENTROIDS[sg + suffix]
    return None, None


def jittered_centroid(
    region: Optional[str], seed: str, spread: float = 0.28
) -> tuple[Optional[float], Optional[float]]:
    """좌표 없는 축제를 광역 중심점 주변에 결정적으로 분산 배치.

    같은 지역의 축제 마커들이 한 점에 겹치지 않도록 seed(축제명 등) 기반
    해시로 재현 가능한 오프셋을 준다. (정확한 위치가 아닌 근사치임)
    """
    lat, lng = region_centroid(region)
    if lat is None or lng is None:
        return None, None
    h = 0
    for ch in seed:
        h = (h * 131 + ord(ch)) & 0xFFFFFFFF
    dx = ((h & 0xFFFF) / 0xFFFF - 0.5) * 2 * spread
    dy = (((h >> 16) & 0xFFFF) / 0xFFFF - 0.5) * 2 * spread
    return round(lat + dy, 6), round(lng + dx, 6)
